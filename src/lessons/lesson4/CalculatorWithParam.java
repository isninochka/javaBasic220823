package lessons.lesson4;

public class CalculatorWithParam {
    public double add (double x, double y){
        double result = x+y;
        return result;
    }

    public double sub (double x, double y){
        double result = x-y;
        return result;
    }

    public double mult (double x, double y){
        double result = x*y;
        return result;
    }
    public double div (double x, double y){
        double result = x/y;
        return result;
    }
}
